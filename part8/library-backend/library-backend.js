const typeDefs = `
  type Book {
    title: String!
    published: Int!
    author: String!
    id: ID!
    genres: [String!]!
  }

  type Author {
    name: String!
    id: ID!
    born: Int
    bookCount: Int!
  }
  
  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      published: Int!
      author: String!
      genres: [String!]
      born: Int
    ):Book

    editAuthor(
      name:String !
      setBornTo: Int!
    ): Author
  }
`


const resolvers = {
  Query: {
    bookCount: () => books.length,
    authorCount: () => authors.length,
    allAuthors : () => authors,
    allBooks: (root,args) => {
      return books.filter(book => {
        const matchesAuthor = args.author ? book.author === args.author : true;
        const matchesGenre = args.genre ? book.genres.includes(args.genre) : true;

        return matchesAuthor && matchesGenre
      })
    },
  },
  Author:{
    bookCount:(root) => books.filter(book => book.author === root.name).length
  },
  Mutation: {
    addBook: (root,args) => {
      let author = authors.find(a => a.name === args.author)
      
      if (!author) {
        author = {
            name: args.author,
            id: uuid(),
            born : args.born !== undefined ? args.born : null,
            bookCount: 0
        };
        authors.push(author); 
      }
    

      const book = { ...args,id:uuid() }
      books = books.concat(book)
      author.bookCount += 1
      return book
    },
    
     editAuthor: (root,args) => {
      const author =  authors.find(a => a.name === args.name)
      if(author){
        author.born = args.setBornTo
        return author
      }
      return null

    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

startStandaloneServer(server, {
  listen: { port: 4000 },
}).then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
